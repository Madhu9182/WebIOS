package Work;

public class StaticDecreIncre {

	public static void main(String[] args) {
		int number_one = 43;
		int number_two = 32;
		//Printing before Decrement operator
		System.out.println(number_one);
		System.out.println(number_two);
		
		number_one--;
		number_two--;
		//Printing after Decrement operator
		System.out.println(number_one);
		System.out.println(number_two);
		
		number_one--;
		number_two--;
		System.out.println(number_one);
		System.out.println(number_two);

	}

}
